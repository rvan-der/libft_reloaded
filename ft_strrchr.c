#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		last;

	i = -1;
	last = -1;
	while (s[++i] != '\0')
		if (s[i] == c)
			last = i;
	if (c == 0)
		return ((char*)(s + i));
	if (last != -1)
		return ((char*)(s + last));
	return (NULL);
}

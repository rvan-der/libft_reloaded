#include "libft.h"

void		ft_putnbr(int n)
{
	if (n < -2147483647)
		write(1, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar('0' + n % 10);
	}
}
